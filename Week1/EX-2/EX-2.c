#include <stdio.h>

int main() {
    int a = 6, b = 3;
    int sum = a + b;
    int diff = a - b;
    int prod = a * b;
    int division = a / b;

    printf("Sum: %d\n", sum);
    printf("Difference: %d\n", diff);
    printf("Product: %d\n", prod);
    printf("Division: %d\n", division);

    return 0;
}
